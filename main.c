#include <stdio.h>    // برای استفاده از printf و scanf
#include <stdlib.h>   // برای استفاده از rand() و srand()
#include <time.h>     // برای استفاده از time() که عدد تصادفی واقعی بسازیم

int main() {                          // شروع تابع اصلی برنامه
    srand(time(0));                   // تنظیم "دانه" تولید عدد تصادفی بر اساس زمان فعلی
                                      // این خط باعث می‌شه هر بار اجرا عدد متفاوتی بگیریم

    int secret = rand() % 100 + 1;    // عدد مخفی: rand() عدد 0..RAND_MAX می‌ده
                                      // %100 → 0 تا 99، +1 → 1 تا 100

    int guess;                        // متغیر برای ذخیره حدس کاربر (هنوز مقدار نداره)
    int tries = 0;                    // شمارنده تعداد حدس‌ها، از صفر شروع می‌شه

    printf("عدد 1-100 رو حدس بزن!\n");  // پیام خوش‌آمدگویی

    do {                                  // شروع حلقه do-while
                                          // حداقل یک بار اجرا می‌شه
        printf("حدست: ");                // درخواست حدس از کاربر
        scanf("%d", &guess);              // خواندن عدد وارد شده و ذخیره در guess
                                          // &guess یعنی آدرس متغیر guess در حافظه

        tries++;                          // هر بار که حدس می‌زنی، تعداد حدس‌ها یکی اضافه می‌شه

        if (guess < secret)               // اگر حدس کمتر از عدد مخفی بود
            printf("بالاتر\n");          // بگو بالاتر برو

        else if (guess > secret)          // اگر حدس بیشتر از عدد مخفی بود
            printf("پایین‌تر\n");        // بگو پایین‌تر بیا

        // اگر guess == secret بود، هیچ پیغامی چاپ نمی‌شه و حلقه تمام می‌شه

    } while (guess != secret);            // تا وقتی حدس اشتباه است، ادامه بده
                                          // وقتی درست شد، شرط false می‌شه و خارج می‌شیم

    printf("آفرین! %d حدس\n", tries);   // تبریک + نمایش تعداد کل حدس‌ها
                                          // %d جای tries قرار می‌گیره

    return 0;                             // پایان موفق برنامه (0 = بدون خطا)
}                                         // پایان تابع main